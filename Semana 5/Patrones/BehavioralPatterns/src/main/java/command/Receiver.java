package command;

public class Receiver {

    public void cut() {
        System.out.println("Obteniendo texto a cortar ...");
        System.out.println("Enviando al portapapapeles ...");
        System.out.println("Cortando cadena ...");
    }

    public void copy() {
        System.out.println("Obteniendo texto a copiar ...");
        System.out.println("Enviando al portapapapeles ...");
    }

    public void paste() {
        System.out.println("Revisando el portapapapeles ...");
        System.out.println("Escribiendo la informacion obtenida del portapapeles ...");
    }
}
